<script>
  gsap.registerPlugin(ScrollToPlugin, Observer);

  /* ── HAMBURGER ── */
  const hamburger   = document.getElementById('hamburger');
  const mobileMenu  = document.getElementById('mobileMenu');

  function openMenu() {
    hamburger.classList.add('open');
    hamburger.setAttribute('aria-expanded', 'true');
    mobileMenu.classList.add('open');
    document.body.classList.add('menu-open');
  }
  function closeMenu() {
    hamburger.classList.remove('open');
    hamburger.setAttribute('aria-expanded', 'false');
    mobileMenu.classList.remove('open');
    document.body.classList.remove('menu-open');
  }
  hamburger.addEventListener('click', () => {
    hamburger.classList.contains('open') ? closeMenu() : openMenu();
  });
  // Close on Escape
  document.addEventListener('keydown', e => { if (e.key === 'Escape') closeMenu(); });

  /* ── DETECT MOBILE ── */
  const isMobile = () => window.innerWidth <= 768;

  /* ── SCROLL SNAP & STAGGERED REVEAL ── */
  let sections        = gsap.utils.toArray(".section");
  let featuresWrapper = document.getElementById('featuresWrapper');
  let gallery         = document.getElementById('gallery');
  let targets         = [...sections, featuresWrapper];
  let currentIndex    = 0;
  let subStep         = 0; // 0: Image Only, 1: Text Visible
  let animating       = false;
  let lastStateChange = 0;
  let observer;

  function setCardState(sectionIndex, show) {
    const card = targets[sectionIndex].querySelector('.card');
    if (card) {
      if (show) card.classList.add('in');
      else card.classList.remove('in');
    }
  }

  function goToSection(index, startSubStep = 0) {
    if (index < 0 || index >= targets.length || animating) return;
    
    animating = true;
    currentIndex = index;
    subStep = startSubStep;
    
    gsap.killTweensOf(window);
    document.documentElement.style.scrollSnapType = 'none';

    targets.forEach((t, i) => { if (i !== currentIndex) setCardState(i, false); });

    gsap.to(window, {
      scrollTo: { y: Math.ceil(targets[index].offsetTop), autoKill: false },
      duration: 1.2, ease: "power2.inOut", overwrite: true,
      onComplete: () => {
        animating = false;
        document.documentElement.style.scrollSnapType = '';
        if (subStep === 1 && currentIndex < sections.length) setCardState(currentIndex, true);
        else if (subStep === 0 && currentIndex < sections.length) setCardState(currentIndex, false);
      }
    });
  }

  /* Helper: is the features section centered in the viewport? */
  function isFeatCentered() {
    const el = document.getElementById('features');
    if (!el) return false;
    const rect = el.getBoundingClientRect();
    const elCenter = rect.top + rect.height / 2;
    const vpCenter = window.innerHeight / 2;
    return Math.abs(elCenter - vpCenter) < 100;
  }

  observer = Observer.create({
    type: "wheel,touch,pointer",
    onDown: () => {
      if (animating) return;
      const now = Date.now();
      if (now - lastStateChange < 600) return;

      // S1-S6
      if (currentIndex < sections.length) {
        if (subStep === 0) {
          setCardState(currentIndex, true);
          subStep = 1;
          lastStateChange = now;
          return;
        } else {
          lastStateChange = now;
          goToSection(currentIndex + 1, 0);
          return;
        }
      }

      // Features Wrapper — only scroll items when centered
      if (targets[currentIndex] === featuresWrapper && isFeatCentered()) {
        if (currentFeat < items.length - 1) {
          updateFeatureItem(currentFeat + 1);
          lastStateChange = now;
          return;
        } else {
          lastStateChange = now;
          goToGallery();
          return;
        }
      }
    },
    onUp: () => {
      if (animating) return;
      const now = Date.now();
      if (now - lastStateChange < 600) return;

      // Gallery -> Features
      if (window.scrollY > gallery.offsetTop - 50) {
        goToSection(targets.length - 1, 0);
        return;
      }

      // Features -> Steps or S6
      if (targets[currentIndex] === featuresWrapper) {
        if (currentFeat > 0) {
          updateFeatureItem(currentFeat - 1);
          lastStateChange = now;
          return;
        } else {
          lastStateChange = now;
          goToSection(currentIndex - 1, 1);
          return;
        }
      }

      // S1-S6
      if (currentIndex < sections.length) {
        if (subStep === 1) {
          setCardState(currentIndex, false);
          subStep = 0;
          lastStateChange = now;
          return;
        } else {
          lastStateChange = now;
          goToSection(currentIndex - 1, 1);
          return;
        }
      }
    },
    tolerance: 5,
    preventDefault: true
  });

  observer.enable();

  function goToGallery() {
    animating = true;
    gsap.to(window, {
      scrollTo: { y: gallery.offsetTop, autoKill: false },
      duration: 1.2, ease: "power2.inOut",
      onComplete: () => {
        animating = false;
        observer.disable();
        currentIndex = targets.length;
      }
    });
  }

  /* ── FEATURES LOGIC (Stepped) ── */
  const featureList = document.getElementById('featureList');
  const inner       = document.getElementById('featureItemsInner');
  const items       = document.querySelectorAll('.feature-item');
  const images      = document.querySelectorAll('#imageStack img');
  const counter     = document.getElementById('counterCurrent');
  let   currentFeat = 0;

  function updateFeatureItem(index) {
    if (index < 0 || index >= items.length) return;
    items[currentFeat].classList.remove('active');
    images[currentFeat].classList.remove('active');
    currentFeat = index;
    items[currentFeat].classList.add('active');
    images[currentFeat].classList.add('active');
    if (counter) counter.textContent = String(currentFeat + 1).padStart(2, '0');

    // Smooth internal list movement
    const maxTranslate = Math.max(0, inner.scrollHeight - featureList.clientHeight);
    const progress = currentFeat / (items.length - 1);
    gsap.to(inner, { y: -progress * maxTranslate, duration: 0.8, ease: "power2.out" });

    // Relative highlighting
    const refDistance = (items.length > 1 ? items[1].offsetTop - items[0].offsetTop : 100) * 1.5;
    const focalY = items[currentFeat].offsetTop + items[currentFeat].clientHeight / 2;
    items.forEach(item => {
